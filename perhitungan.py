import math
def tambah(bil1, bil2):
    hasil = bil1+bil2
    print("hasilnya adalah",hasil)

def kurang(bil1, bil2):
    hasil = bil1-bil2
    print("hasilnya adalah",hasil)

def bagi(bil1, bil2):
    hasil = bil1/bil2
    print("hasilnya adalah",hasil)

def kali(bil1, bil2):
    hasil = bil1*bil2
    print("hasilnya adalah",hasil)

def pangkat(bil1, bil2):
    hasil = bil1**bil2
    print("hasilnya adalah",hasil)

def akar(bil1):
    hasil = math.sqrt(bil1)
    print("hasilnya adalah",hasil)

def log(bil1):
    hasil = math.log(bil1)
    print("hasilnya adalah", hasil)

def sin(bil1):
    hasil = math.sin(bil1)
    print("hasilnya adalah", hasil)

def cos(bil1):
    hasil = math.cos(bil1)
    print("hasilnya adalah", hasil)

def tan(bil1):
    hasil = math.tan(bil1)
    print("hasilnya adalah", hasil)


